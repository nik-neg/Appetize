const db = require('./db');

const ImageSchema = new db.mongoose.Schema({
  length: Number,
  chunkSize: Number,
  uploadDate: Date,
  filename: String,
  md5: String,
  contentType: String,
  image: { data: Buffer, contentType: String },
});
const ImageModel = db.mongoose.model('Image', ImageSchema);

module.exports = ImageModel;
